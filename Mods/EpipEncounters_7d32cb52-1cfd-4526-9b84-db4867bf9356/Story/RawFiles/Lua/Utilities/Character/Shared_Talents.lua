-- TALENT
---------------------------------------------

---@class CharacterLib_Talent
---@field NameHandle TranslatedStringHandle
---@field DescriptionHandle TranslatedStringHandle
---@field ID string
---@field NumericID integer
local _Talent = {}

function _Talent:GetName()
    local name = self.ID

    if self.NameHandle then
        name = Ext.L10N.GetTranslatedString(self.NameHandle, name)
    end

    return name
end

function _Talent:GetDescription()
    local desc = ""

    if self.DescriptionHandle then
        desc = Ext.L10N.GetTranslatedString(self.DescriptionHandle, desc)
        desc = desc:gsub("%[1%]", self:GetName())
    end

    return desc
end

---------------------------------------------
-- SETUP
---------------------------------------------

-- Initialize talent tables.
for id,tbl in pairs(Character.Talents) do
    Inherit(tbl, _Talent)

    tbl.ID = id
end